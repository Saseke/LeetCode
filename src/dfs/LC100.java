package dfs;

import tree.TreeNode;

/**
 * @Author Yoke
 * @Date 2019/01/21 上午9:28
 */
public class LC100 {
    public boolean isSameTree(TreeNode p, TreeNode q) {
        if (p == null && q == null) {
            return true;
        }
        if (p == null || q == null) {
            return false;
        }
        if (p.val != q.val) {
            return false;
        }
        return isSameTree(p.left, q.left) && isSameTree(p.right, q.right);
    }

}
